#O número do raio com casas decimais a mais. Float(casas decimais) = valor a ser atribuido na entrada 
raio = float(input()) 
n = 3.14159 #O número que multiplicada pelo raio da o resultado da área. O número ao qual a váriavel N foi atribuida 
print(f'A={n * raio**2:.4f}') #Resultado com a conta feita para achar áreas de circulos, com 4 casas decimais a mais imprimindo. O saida.